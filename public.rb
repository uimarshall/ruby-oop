class Car
  CAR_YEARS = 7

  attr_accessor :name, :age

  def initialize(n, a)
    self.name = n
    self.age = a
  end

  private

  def human_years
    age * CAR_YEARS
  end
end

sparky = Car.new("Sparky", 4)
sparky.human_years


